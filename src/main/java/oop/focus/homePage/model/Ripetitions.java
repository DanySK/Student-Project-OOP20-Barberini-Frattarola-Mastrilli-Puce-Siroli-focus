package oop.focus.homePage.model;

public enum Ripetitions {

        /**
         * 
         * The event never repeat.
         */
	NEVER,
	/**
	 * 
	 * The event is repeated once a year.
	 */
	EVERY_YEAR,
	/**
         * 
         * The event is repeated once a half year(where half a year equals six months).
         */
	EVERY_HALF_YEAR,
	/**
	 * 
	 * The event is repeated once a month.
	 */
	EVERY_MONTH,
	/**
	 * 
	 * The event is repeated once a week.
	 */
	EVERY_WEEK,
	/**
	 * 
	 * The event is repeated once a day.
	 */
	EVERY_DAY;
}
